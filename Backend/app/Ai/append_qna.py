def load_data():
    with open(AI_JSON_PATH, "r", encoding="utf-8") as f:
        return json.load(f)

def save_data(data):
    with open(AI_JSON_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def main():
    data = load_data()
    
    # Starting ID
    next_id = 1301
    
    # Process greetings
    for kw in greetings_keywords:
        qna_key = f"QNA{next_id}"
        translations = {}
        for lang, t_ans in greetings_ans.items():
            translations[lang] = {
                "question": kw,
                "answer": t_ans
            }
        data[qna_key] = {"translations": translations}
        next_id += 1

    # Process goodbyes
    for kw in goodbye_keywords:
        qna_key = f"QNA{next_id}"
        translations = {}
        for lang, t_ans in goodbye_ans.items():
            translations[lang] = {
                "question": kw,
                "answer": t_ans
            }
        data[qna_key] = {"translations": translations}
        next_id += 1
        
    save_data(data)
    print(f"Added new entries to {AI_JSON_PATH}. Next ID to use would be QNA{next_id}")

if __name__ == "__main__":
    main()
