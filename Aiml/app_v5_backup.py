_RANGES_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "feature_ranges.json")
with open(_RANGES_PATH, encoding="utf-8") as _f:
    FEATURE_RANGES: dict = json.load(_f)

_ACC = FEATURE_RANGES["acceptance"]            # hard input-gate for Pydantic
_ORIG_FEAT = FEATURE_RANGES["original"]["features"]   # per-model OOD guard
_SYNTH_FEAT = FEATURE_RANGES["synthetic"]["features"]

logger.info("Loaded feature_ranges.json  acceptance=%s",
            {k: [v["min"], v["max"]] for k, v in _ACC.items() if not k.startswith("_")})

# Per-model training ranges (for OOD warnings, NOT hard rejection)
TRAINING_RANGES = {
    "original":  {k: {"min": v["min"], "max": v["max"]} for k, v in _ORIG_FEAT.items()},
    "synthetic": {k: {"min": v["min"], "max": v["max"]} for k, v in _SYNTH_FEAT.items()},
}
TRAINING_RANGES["both"] = {
    feat: {
        "min": min(
            TRAINING_RANGES["original"].get(feat, {}).get("min", 999),
            TRAINING_RANGES["synthetic"].get(feat, {}).get("min", 999),
        ),
        "max": max(
            TRAINING_RANGES["original"].get(feat, {}).get("max", -999),
            TRAINING_RANGES["synthetic"].get(feat, {}).get("max", -999),
        ),
    }
    for feat in set(
        list(TRAINING_RANGES["original"]) + list(TRAINING_RANGES["synthetic"])
    )
}


