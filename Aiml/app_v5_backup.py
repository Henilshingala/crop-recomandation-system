VALID_MODES = {"original", "synthetic", "both"}


class PredictionInput(BaseModel):
    """Pydantic input gate — limits sourced from feature_ranges.json acceptance ranges."""
    N: float = Field(..., ge=_ACC["N"]["min"], le=_ACC["N"]["max"], description="Nitrogen (kg/ha)")
    P: float = Field(..., ge=_ACC["P"]["min"], le=_ACC["P"]["max"], description="Phosphorus (kg/ha)")
    K: float = Field(..., ge=_ACC["K"]["min"], le=_ACC["K"]["max"], description="Potassium (kg/ha)")
    temperature: float = Field(..., ge=_ACC["temperature"]["min"], le=_ACC["temperature"]["max"], description="Temperature (°C)")
    humidity: float = Field(..., ge=_ACC["humidity"]["min"], le=_ACC["humidity"]["max"], description="Humidity (%)")
    ph: float = Field(..., ge=_ACC["ph"]["min"], le=_ACC["ph"]["max"], description="Soil pH")
    rainfall: float = Field(..., ge=_ACC["rainfall"]["min"], le=_ACC["rainfall"]["max"], description="Rainfall (mm)")
    moisture: Optional[float] = Field(43.5, ge=_ACC["moisture"]["min"], le=_ACC["moisture"]["max"], description="Soil moisture (%)")
    season: Optional[int] = Field(None, ge=_ACC["season"]["min"], le=_ACC["season"]["max"], description="0=Kharif, 1=Rabi, 2=Zaid")
    soil_type: Optional[int] = Field(1, ge=_ACC["soil_type"]["min"], le=_ACC["soil_type"]["max"], description="Soil type")
    irrigation: Optional[int] = Field(0, ge=_ACC["irrigation"]["min"], le=_ACC["irrigation"]["max"], description="0=rainfed, 1=irrigated")
    mode: Optional[str] = Field("original", description="original | synthetic | both")
    top_n: Optional[int] = Field(3, ge=1, le=10, description="Top N predictions")


@app.exception_handler(RequestValidationError)
async def validation_exception_handler(request: Request, exc: RequestValidationError):
    return JSONResponse(
        status_code=422,
        content={"detail": exc.errors(), "message": "Invalid input"},
    )


