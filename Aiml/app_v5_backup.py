# ── STARTUP ASSERTIONS ─────────────────────────────────────────────────

def _assert_startup():
    errors = []

    # ── Model assertions ──
    if _original:
        if _original.crop_count != 19:
            errors.append(f"Original model has {_original.crop_count} crops, expected 19")
    else:
        errors.append("Original model not loaded")
    if _synthetic:
        if _synthetic.crop_count != 51:
            errors.append(f"Synthetic model has {_synthetic.crop_count} crops, expected 51")
    else:
        errors.append("Synthetic model not loaded")
    if not _both:
        errors.append("Hybrid predictor not available")

    # ── Feature-range integrity assertions ──
    required_sections = ["acceptance", "original", "synthetic"]
    for sec in required_sections:
        if sec not in FEATURE_RANGES:
            errors.append(f"feature_ranges.json missing '{sec}' section")

    core_features = ["N", "P", "K", "temperature", "humidity", "ph", "rainfall"]
    for feat in core_features:
        if feat not in _ACC:
            errors.append(f"Acceptance ranges missing feature '{feat}'")
        elif _ACC[feat]["min"] >= _ACC[feat]["max"]:
            errors.append(f"Invalid acceptance range for {feat}: {_ACC[feat]}")

    logger.info(
        "Feature acceptance ranges: %s",
        {k: [v["min"], v["max"]] for k, v in _ACC.items() if not k.startswith("_")},
    )
    logger.info(
        "Original training ranges (OOD guard): %s",
        {k: [v["min"], v["max"]] for k, v in TRAINING_RANGES["original"].items()},
    )
    logger.info(
        "Synthetic training ranges (OOD guard): %s",
        {k: [v["min"], v["max"]] for k, v in TRAINING_RANGES["synthetic"].items()},
    )

    for e in errors:
        logger.error("STARTUP ASSERTION FAILED: %s", e)
    if not errors:
        logger.info("All startup assertions passed.")

_assert_startup()


